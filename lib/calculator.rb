# frozen_string_literal: true

class Calculator
    def somar(numero1, numero2)
        numero1 + numero2
    end

    def subtrair(numero1, numero2)
        numero1 - numero2
    end

    def multiplicar(numero1, numero2)
        numero1 * numero2
    end

    def dividir(numero1, numero2)
        numero1 / numero2
    end

    def modulo(numero1, numero2)
        numero1 % numero2
    end

    def expoente(numero1, numero2)
        numero1**numero2
    end
end
